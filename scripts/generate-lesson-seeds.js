#!/usr/bin/env node
/**
 * generate-lesson-seeds.js
 *
 * Reads all .md files from course-content/{COURSE_CODE}/ folders,
 * parses optional YAML frontmatter, converts Markdown to HTML,
 * and generates a SQL migration file that seeds the lessons table.
 *
 * Usage:
 *   node scripts/generate-lesson-seeds.js
 *
 * Output:
 *   supabase/migrations/20260416000003_lesson_content_seed.sql
 */

import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, "..");
const CONTENT_DIR = path.join(ROOT, "course-content");
const OUTPUT_FILE = path.join(ROOT, "supabase/migrations/20260416000003_lesson_content_seed.sql");

// ── Minimal Markdown → HTML converter (no external deps needed) ──────────────
function markdownToHtml(md) {
  let html = md
    // Fenced code blocks
    .replace(/```(\w*)\n([\s\S]*?)```/g, (_, lang, code) => {
      const escaped = code.replace(/&/g, "&amp;").replace(/</g, "&lt;").replace(/>/g, "&gt;");
      return `<pre><code${lang ? ` class="language-${lang}"` : ""}>${escaped}</code></pre>`;
    })
    // Inline code
    .replace(/`([^`]+)`/g, "<code>$1</code>")
    // Headings
    .replace(/^### (.+)$/gm, "<h3>$1</h3>")
    .replace(/^## (.+)$/gm, "<h2>$1</h2>")
    .replace(/^# (.+)$/gm, "<h1>$1</h1>")
    // Bold
    .replace(/\*\*(.+?)\*\*/g, "<strong>$1</strong>")
    // Italic
    .replace(/\*(.+?)\*/g, "<em>$1</em>")
    // Unordered lists
    .replace(/^- (.+)$/gm, "<li>$1</li>")
    .replace(/(<li>.*<\/li>\n?)+/g, (m) => `<ul>\n${m}</ul>\n`)
    // Ordered lists
    .replace(/^\d+\. (.+)$/gm, "<li>$1</li>")
    // Horizontal rule
    .replace(/^---$/gm, "<hr>")
    // Paragraphs (lines not already wrapped in a tag)
    .replace(/^(?!<[a-z]|\s*$)(.+)$/gm, "<p>$1</p>")
    // Clean up extra blank lines
    .replace(/\n{3,}/g, "\n\n")
    .trim();

  return html;
}

// ── Parse YAML frontmatter ────────────────────────────────────────────────────
function parseFrontmatter(content) {
  const match = content.match(/^---\n([\s\S]*?)\n---\n([\s\S]*)$/);
  if (!match) return { meta: {}, body: content };

  const meta = {};
  for (const line of match[1].split("\n")) {
    const [key, ...rest] = line.split(":");
    if (key && rest.length) {
      meta[key.trim()] = rest.join(":").trim().replace(/^["']|["']$/g, "");
    }
  }
  return { meta, body: match[2] };
}

// ── Slug → title ──────────────────────────────────────────────────────────────
function slugToTitle(slug) {
  return slug
    .replace(/^\d+-/, "")           // remove leading order number
    .replace(/-/g, " ")             // hyphens to spaces
    .replace(/\b\w/g, (c) => c.toUpperCase()); // title case
}

// ── Escape SQL string ─────────────────────────────────────────────────────────
function sqlStr(s) {
  if (s === null || s === undefined) return "NULL";
  return `'${String(s).replace(/'/g, "''")}'`;
}

// ── Main ──────────────────────────────────────────────────────────────────────
function main() {
  if (!fs.existsSync(CONTENT_DIR)) {
    console.error(`course-content/ folder not found at ${CONTENT_DIR}`);
    process.exit(1);
  }

  const courseFolders = fs.readdirSync(CONTENT_DIR)
    .filter((f) => {
      const full = path.join(CONTENT_DIR, f);
      return fs.statSync(full).isDirectory() && f !== "README.md";
    })
    .sort();

  if (courseFolders.length === 0) {
    console.log("No course folders found in course-content/. Nothing to generate.");
    process.exit(0);
  }

  const lines = [
    "-- ============================================================",
    "-- Lesson Content Seed — auto-generated by generate-lesson-seeds.js",
    `-- Generated: ${new Date().toISOString()}`,
    "-- ============================================================",
    "",
    "-- Delete existing lessons for these courses before re-seeding",
  ];

  const allInserts = [];

  for (const courseCode of courseFolders) {
    const courseDir = path.join(CONTENT_DIR, courseCode);
    const mdFiles = fs.readdirSync(courseDir)
      .filter((f) => f.endsWith(".md"))
      .sort();

    if (mdFiles.length === 0) continue;

    console.log(`Processing ${courseCode}: ${mdFiles.length} lessons`);

    lines.push(`DELETE FROM public.lessons WHERE "CourseId" IN (`);
    lines.push(`  SELECT "Id" FROM public.courses WHERE "CourseCode" = '${courseCode}'`);
    lines.push(`);`);
    lines.push("");

    lines.push(`-- ${courseCode} lessons`);
    lines.push(`INSERT INTO public.lessons`);
    lines.push(`  ("CourseId", "Title", "Content", "VideoUrl", lesson_type, "OrderIndex", "DurationMinutes", "IsPublished")`);
    lines.push(`SELECT`);
    lines.push(`  c."Id",`);

    const lessonRows = [];

    for (let i = 0; i < mdFiles.length; i++) {
      const file = mdFiles[i];
      const raw = fs.readFileSync(path.join(courseDir, file), "utf8");
      const { meta, body } = parseFrontmatter(raw);

      const slug = path.basename(file, ".md");
      const orderMatch = slug.match(/^(\d+)/);
      const orderIndex = orderMatch ? parseInt(orderMatch[1], 10) : i + 1;

      const title = meta.title || slugToTitle(slug);
      const lessonType = meta.type || "text";
      const duration = meta.duration ? parseInt(meta.duration, 10) : 30;
      const videoUrl = meta.videoUrl || null;
      const html = markdownToHtml(body);

      lessonRows.push(
        `  (${sqlStr(title)}, ${sqlStr(html)}, ${sqlStr(videoUrl)}, ${sqlStr(lessonType)}, ${orderIndex}, ${duration}, true)`
      );

      allInserts.push({ courseCode, title, orderIndex });
    }

    // Build the full INSERT ... SELECT
    lines.push(`  v.title, v.content, v.video_url::text, v.lesson_type, v.order_index, v.duration_minutes, v.is_published`);
    lines.push(`FROM public.courses c`);
    lines.push(`CROSS JOIN (VALUES`);
    lines.push(lessonRows.join(",\n"));
    lines.push(`) AS v(title, content, video_url, lesson_type, order_index, duration_minutes, is_published)`);
    lines.push(`WHERE c."CourseCode" = '${courseCode}';`);
    lines.push("");
  }

  // Summary
  lines.push(`-- Summary: ${allInserts.length} lessons across ${courseFolders.length} courses`);

  fs.writeFileSync(OUTPUT_FILE, lines.join("\n"), "utf8");
  console.log(`\n✅ Generated: ${OUTPUT_FILE}`);
  console.log(`   ${allInserts.length} lessons across ${courseFolders.length} courses`);
  console.log(`\nApply with:`);
  console.log(`  & "C:\\Program Files\\PostgreSQL\\15\\bin\\psql.exe" -U postgres -d adu-africa -f "${OUTPUT_FILE}"`);
}

main();
